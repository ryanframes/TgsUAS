    private void cmdDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdDeleteActionPerformed
        // TODO add your handling code here:
        if (txtKode.isEnabled()) {
            cls.showMsg("Harap pilih barang yang akan dihapus !", "Hapus Gagal", 2);
            txtKode.grabFocus();
            return;
        }
        int btn = JOptionPane.YES_NO_OPTION;
        int res=JOptionPane.showConfirmDialog(null,"Apakah anda yakin ingin "
            + "menghapus data terpilih ?","Hapus Data",btn);
        if (res==JOptionPane.YES_OPTION){
            try {
                oConn.setData("delete from t_barang where barang_kode = '"
                    + txtKode.getText() + "'");
                cmdDisplay.doClick();
                cls.showMsg("Barang ["+ txtKode.getText() +" - "+ txtNama.getText() +"] "
                    + "berhasil di hapus.", "Hapus Berhasil", 0);
                textMode(true);
                clrForm();
            } catch (SQLException ex) {
                cls.showMsg(ex.getMessage(), "Error", 0);
            }
        }
    }//GEN-LAST:event_cmdDeleteActionPerformed

