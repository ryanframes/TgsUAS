    private void cmdSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdSaveActionPerformed
        // TODO add your handling code here:
        if (!allowSave()){ return; }
        try {
            if (txtKode.isEnabled()) {
                oConn.setData("insert into t_barang (barang_kode, barang_nama, "
                    + "no_rak) values ('" + txtKode.getText() + "','" + txtNama.getText() + "', "
                    + "'" + txtNoRak.getText() + "')");
            }
            else {
                oConn.setData("update t_barang set barang_nama = '" + txtNama.getText() + "', "
                    + "no_rak = '" + txtNoRak.getText() + "' where barang_kode = '" + txtKode.getText() + "'");
            }
            clrForm();
            cls.showMsg("Simpan data berhasil !", "Simpan", 1);
        } catch (SQLException ex) {
            cls.showMsg(ex.getSQLState(), "Gagal Simpan", 0);
        }
        cmdDisplay.doClick();
    }//GEN-LAST:event_cmdSaveActionPerformed

