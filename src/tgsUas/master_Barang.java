    private void cmdGenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdGenActionPerformed
        // TODO add your handling code here:
        String lastNo;
        ResultSet rs=oConn.getData("select ifnull(max(cast(right(barang_kode,5) as signed)),0)+1 as last_no from t_barang where "
                + "barang_kode like '%MAT-%' order by barang_kode desc");
        try {
            rs.next();
            lastNo="MAT-" + ("00000").substring(rs.getString("last_no").length()) + rs.getString("last_no");
            txtKode.setText(lastNo);
        } catch (SQLException ex) {
            Logger.getLogger(master_Barang.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_cmdGenActionPerformed
    private boolean allowSave() {
        if (txtKode.getText().equals("")) {
            cls.showMsg("Kode Barang harus di isi", "Simpan Gagal", 0);
            txtKode.grabFocus();
            return false;
        }
        if (txtNama.getText().equals("")) {
            cls.showMsg("Nama Barang harus di isi", "Simpan Gagal", 0);
            txtNama.grabFocus();
            return false;
        }
        if (txtNoRak.getText().equals("")) {
            cls.showMsg("No. Rak harus di isi", "Simpan Gagal", 0);
            txtNoRak.grabFocus();
            return false;
        }
        return true;
    }    
    private void clrForm() {
        txtKode.setText("");
        txtNama.setText("");
        txtNoRak.setText("");
        txtKode.grabFocus();
    }
        
    private void textMode(boolean bol) {
        txtKode.setEnabled(bol);
        txtNama.setEnabled(bol);
        txtNoRak.setEnabled(bol);
    }
    

